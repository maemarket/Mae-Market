join factura ON relacion_persona_rol.persona_n_persona=factura.persona_rol_factura
join detalle_factura ON factura.n_factura=detalle_factura.factura_n_factura
join productos ON detalle_factura.cod_producto_detalle=productos.cod_producto
where direccion like '%Calle%';

